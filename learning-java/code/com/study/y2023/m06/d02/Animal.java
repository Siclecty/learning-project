package com.learning.objecttest;

public class Animal {
    private String name;
    private double age;

    public Animal() {
    }

    public Animal(String name, double age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getAge() {
        return age;
    }

    public void setAge(double age) {
        this.age = age;
    }
}
